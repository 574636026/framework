package com.injedu.imagesearch.entity;

/**
 * 下载任务
 *
 * @author joy.zhou
 * @date 2016年3月12日
 * @version 1.0
 *
 */
public class DownloadTask {
	/** 对应标识 */
	private Long id;
	/** 对应字段名 */
	private String column;
	/** 下载地址 */
	private String url;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getColumn() {
		return column;
	}

	public void setColumn(String column) {
		this.column = column;
	}

	@Override
	public String toString() {
		return "DownloadTask [id=" + id + ", column=" + column + ",url=" + url + "]";
	}

}
