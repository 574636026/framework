package com.injedu.supports.push.constant;

/**
 * 消息类型
 *
 * @author joy.zhou
 * @date 2015年11月2日
 * @version 1.0
 *
 */
public enum NoticeType {
	/** 通知 */
	NOTIFICATION(1),
	/** 穿透消息 */
	MESSAGE(2);

	private int type;

	NoticeType(int type) {
		this.type = type;
	}

	public int getType() {
		return this.type;
	}

}
