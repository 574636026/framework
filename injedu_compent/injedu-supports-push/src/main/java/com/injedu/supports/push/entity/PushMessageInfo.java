package com.injedu.supports.push.entity;

/**
 * 推送消息信息
 *
 * @author joy.zhou
 * @date 2015年11月7日
 * @version 1.0
 *
 */
public class PushMessageInfo {

	/** 消息ID */
	private String taskId;
	/** 消息状态 */
	private Integer status;
	/** 推送数量 */
	private Integer pushCount;
	/** 到达数量 */
	private Integer arriveCount;
	/** 点击数量 */
	private Integer clickCount;
	/** 删除数量 */
	private Integer delCount;

	public Integer getPushCount() {
		return pushCount;
	}

	public void setPushCount(Integer pushCount) {
		this.pushCount = pushCount;
	}

	public Integer getClickCount() {
		return clickCount;
	}

	public void setClickCount(Integer clickCount) {
		this.clickCount = clickCount;
	}

	public Integer getDelCount() {
		return delCount;
	}

	public void setDelCount(Integer delCount) {
		this.delCount = delCount;
	}

	public String getTaskId() {
		return taskId;
	}

	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Integer getArriveCount() {
		return arriveCount;
	}

	public void setArriveCount(Integer arriveCount) {
		this.arriveCount = arriveCount;
	}

	@Override
	public String toString() {
		return "PushMessageInfo [taskId=" + taskId + ", status=" + status + ", pushCount=" + pushCount
				+ ", arriveCount=" + arriveCount + ", clickCount=" + clickCount + ", delCount=" + delCount + "]";
	}

}
