package com.injedu.core.dto;

import java.io.Serializable;

/**
 * 
 * 供组件使用
 *
 * @author joy.zhou
 * @date 2015年12月1日
 * @version 1.0
 *
 */
public class ItemDto implements Serializable {

	private static final long serialVersionUID = 1942358493089849161L;

	public ItemDto() {
	}

	public ItemDto(String item, Object value) {
		this.item = item;
		this.value = value;
	}

	/** 显示名称 */
	private String item;
	/** 值 */
	private Object value;

	public String getItem() {
		return item;
	}

	public void setItem(String item) {
		this.item = item;
	}

	public Object getValue() {
		return value;
	}

	public void setValue(Object value) {
		this.value = value;
	}

}
