package com.injedu.core.supports.echarts.data;

import java.io.Serializable;

/**
 * 
 * 分组数据
 *
 * @author joy.zhou
 * @date 2016年4月12日
 * @version 1.0
 *
 */
public class CategoryData implements Serializable {

	private static final long serialVersionUID = 1L;
	/** 分组名称 */
	private String name;
	/** 值 */
	private Object value;

	public CategoryData() {
	}

	public CategoryData(String name, Object value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Object getValue() {
		return value;
	}

	public void setValue(Object value) {
		this.value = value;
	}

}
